namespace Tourmi.RLMV.Aseprite;

/// <summary>
/// Data structure used by Aseprite representing a Rectangle. 
/// </summary>
public class AsepriteRectangle
{
    /// <summary>
    /// X location
    /// </summary>
    public int X { get; set; }

    /// <summary>
    /// Y location
    /// </summary>
    public int Y { get; set; }

    /// <summary>
    /// Width
    /// </summary>
    public int W { get; set; }

    /// <summary>
    /// Height
    /// </summary>
    public int H { get; set; }
}
